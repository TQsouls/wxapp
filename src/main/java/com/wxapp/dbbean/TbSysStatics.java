package com.wxapp.dbbean;


public class TbSysStatics {

  private long ssId;
  private long userId;
  private long ssAllOnlineCount;
  private long ssAllFriendCount;
  private long ssAllDeadCount;


  public long getSsId() {
    return ssId;
  }

  public void setSsId(long ssId) {
    this.ssId = ssId;
  }


  public long getUserId() {
    return userId;
  }

  public void setUserId(long userId) {
    this.userId = userId;
  }


  public long getSsAllOnlineCount() {
    return ssAllOnlineCount;
  }

  public void setSsAllOnlineCount(long ssAllOnlineCount) {
    this.ssAllOnlineCount = ssAllOnlineCount;
  }


  public long getSsAllFriendCount() {
    return ssAllFriendCount;
  }

  public void setSsAllFriendCount(long ssAllFriendCount) {
    this.ssAllFriendCount = ssAllFriendCount;
  }


  public long getSsAllDeadCount() {
    return ssAllDeadCount;
  }

  public void setSsAllDeadCount(long ssAllDeadCount) {
    this.ssAllDeadCount = ssAllDeadCount;
  }

}
